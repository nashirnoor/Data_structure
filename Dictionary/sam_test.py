my_dict = {
    "name":"Nashir",
    "age":22,
    "code":172
    }


# Iterating over keys
for key in my_dict:
    print(key)

print("--------------")
# Iterating over values
for value in my_dict.values():
    print(value)

print("---------")
# Iterating over key-value pairs
for key, value in my_dict.items():
    print(key,":", value)


